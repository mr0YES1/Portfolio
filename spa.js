function goTo(index) {
    current = Math.min(pages.length - 1, Math.max(0, index))
    history.replaceState(null, '', `#/page/${current}`)
    render()
}

function readHash() {
    const match = window.location.hash.match(/#\/page\/(\d+)/)
    return match ? parseInt(match[1]) : 0
}
window.addEventListener('load', () => {
    current = readHash()
    return
})


function buildSlider(sources) {
    const cards = sources.map(src => `
    <div class="grabber-el">
      <img src="${src}" alt="pic" class="grabber-image">
    </div>
  `).join('')

    return `<div class="grabber" id="grabber">${cards}</div>`
}

const staticZones = {
}

for (const [id, html] of Object.entries(staticZones)) {
    document.getElementById(id).innerHTML = html
}

let current = readHash()

function render() {
    const page = pages[current]

    for (const [id, html] of Object.entries(page)) {
        const el = document.getElementById(id)
        if (!el) continue
        el.innerHTML = html
        el.classList.remove('flash')
        void el.offsetWidth // reflow
        el.classList.add('flash')
    }
    document.getElementById('nav-prev').disabled = current === 0
    document.getElementById('nav-next').disabled = current === pages.length - 1
}

document.getElementById('nav-prev').onclick = () => { if (current > 0) { goTo(current - 1); render(); resetSlider() } }
document.getElementById('nav-next').onclick = () => { if (current < pages.length - 1) { goTo(current + 1); render(); resetSlider() } }
render()
